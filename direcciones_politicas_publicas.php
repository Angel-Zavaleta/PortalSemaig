<script>
                document.addEventListener("DOMContentLoaded", function () {
                    // Ocultar todos los elementos cuyo ID comience con 'cont-pgm'
                    function hideInfoElements() {
                        var elements = document.querySelectorAll("[id^='cont-pgm']");
                        elements.forEach(function (element) {
                            element.style.display = "none";
                        });
                    }
        
                    // Función para mostrar el elemento correspondiente al hacer clic en un <li>
                    function showInfoElement(event) {
                        var clickedItemId = event.target.id;
                        var infoElementId = "cont-" + clickedItemId;
        
                        // Ocultar todos los elementos primero
                        hideInfoElements();
        
                        // Quitar resaltado de todos los <li>
                        var listItems = document.querySelectorAll("li[id^='pgm']");
                        listItems.forEach(function (item) {
                            item.classList.remove('highlighted2');
                        });
        
                        // Resaltar el <li> clicado
                        event.target.classList.add('highlighted2');
        
                        // Mostrar el elemento correspondiente
                        var infoElement = document.getElementById(infoElementId);
                        if (infoElement) {
                            infoElement.style.display = "block";
        
                            // Desplazar hacia arriba para mostrar la info-item
                            infoElement.scrollIntoView({ behavior: 'smooth', block: 'center', inline: 'nearest' });
                        }
                    }
        
                    // Agregar evento click a cada <li>
                    var listItems = document.querySelectorAll("li[id^='pgm']");
                    listItems.forEach(function (item) {
                        item.addEventListener("click", showInfoElement);
                    });
        
                    // Ocultar todos los elementos 'cont-pgm' al cargar la página
                    hideInfoElements();
                });
</script>

